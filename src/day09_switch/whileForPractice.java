package day09_switch;

public class whileForPractice {

	public static void main(String[] args) {
		//for
		int i=20;
		for (i=20; i<31; i++) {System.out.println(i);}
		//do while
		int e =20;
		do {System.out.println(e);
		e++;
		}while (e<=30);
		//while
		int y =20;
		while (y<=30) {System.out.println(y); y++;}
		System.out.println("current value of y is "+y);
		
		
		
		
	}

}
