package day4_operator;

public class ArithmeticOperator {

	public static void main(String[] args) {
		//+,-,*,/,%,++,__
		float a=3;
		float b=20;
		double c=2;
		
        System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a/c);

        //+
		int valueC=1;
		System.out.println(valueC);
		valueC = valueC +1;
		System.out.println(valueC);
		valueC++;//increment
			System.out.println(valueC);
		valueC--;
		System.out.println(valueC);
	}
	

}
